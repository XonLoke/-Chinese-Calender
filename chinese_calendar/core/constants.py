# ===========================================================================
# ΔT (Delta T) 近似常数
# ===========================================================================
# ΔT = TT - UT (单位: 秒)
# 用于将 UTC 时间转换为力学时间 (TT)，
# 这是天文计算必需的步骤。
#
# 对于 2020-2100 年范围，可使用以下多项式近似:
#   ΔT (秒) = -1.15 + 0.0785*t + 0.0004*t²  其中 t = (year - 2000)
# 来源: Morrison & Stephenson (2004), 更新版
#
# 注意: Sprint 2 的 time_systems.py 会使用更精确的插值表
# 这里仅定义基础常数

# 2020-2100 ΔT 近似系数 (t = year - 2000)

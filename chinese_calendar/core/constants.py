# 二十四节气对应的太阳黄经（度）
# 索引 0 = 小寒 (285°), 索引 23 = 冬至 (270°)
# 实际上节气按黄经 315° (立春) 开始排列更符合传统，
# 但天文计算按 0-360° 顺序处理。
#
# 节气索引到黄经的映射:
#   term_index = [0, 1, 2, ..., 23]
#   longitude = [285, 300, 315, 330, 345, 0, 15, 30, ..., 270]
#   (小寒, 大寒, 立春, ..., 冬至)
#
# 即: longitude = (term_index * 15 + 285) % 360
#   term 0 (小寒): (0*15 + 285) % 360 = 285°
#   term 23 (冬至): (23*15 + 285) % 360 = (345 + 285) % 360 = 630 % 360 = 270°

# 立春对应的黄经
LI_CHUN_LONGITUDE: float = 315.0

# 春分对应的黄经
VERNAL_EQUINOX_LONGITUDE: float = 0.0  # 或 360.0

# 冬至对应的黄经
